"""
Topic: Variables
Example: Character Profile of Naruto Uzumaki (Shippuden)

This file demonstrates how variables store different types of data.
"""

# String variables
name = "Naruto Uzumaki"
village = "Konohagakure"
dream = "Become Hokage"
birth_date = "October 10"

# Integer variable
age_shippuden = 17

# Float variable
height_shippuden = 166.0

# Boolean variable
is_hokage = False

# Display character information
print("Name:", name)
print("Village:", village)
print("Dream:", dream)
print("Birth Date:", birth_date)
print("Age (Shippuden):", age_shippuden)
print("Height (Shippuden):", height_shippuden, "cm")
print("Is Hokage:", is_hokage)

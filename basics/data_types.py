"""
Topic: Data Types
Example: Blue Morpho Butterfly

This file demonstrates the four basic data types in Python.
"""

# String
species = "Blue Morpho"
wing_color = "Iridescent Blue"
habitat = "Tropical Rainforest"

# Integer
life_span_days = 115

# Float
wing_span_cm = 15.2

# Boolean
is_endangered = False

# Display butterfly information
print("Species:", species)
print("Wing Color:", wing_color)
print("Habitat:", habitat)
print("Life Span Days:", life_span_days)
print("Wing Span:", wing_span_cm, "cm")
print("Is Endangered:", is_endangered)

print()

# Display data types
print("Data Types")
print("species:", type(species))
print("wing_color:", type(wing_color))
print("habitat:", type(habitat))
print("life_span_days:", type(life_span_days))
print("wing_span_cm:", type(wing_span_cm))
print("is_endangered:", type(is_endangered))
